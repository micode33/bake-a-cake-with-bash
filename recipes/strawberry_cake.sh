strawberry_cake(){
  
  echo -e "${GREEN}${BOLD}STRAWBERRY CAKE${CLEAR}"
  echo
  echo -e "${CYAN}${BOLD}INGREDIENTS${CLEAR}"
  echo
  echo -e "${YELLOW}${BOLD}Cake:${CLEAR}"
  echo "- 2 1/2 cups (375g) self-raising flour"
  echo "- 3 tsp baking powder"
  echo "- 1/2 tsp salt"
  echo "- 6 eggs (59g each), at room temperature"
  echo "- 375g unsalted butter, cubed, softened"
  echo "- 375g Chelsea Caster Sugar"
  echo "- Finely grated rind and juice of 2 mandarins"
  echo
  echo -e "${YELLOW}${BOLD}Filling & Icing:${CLEAR}"
  echo "- 4 x 250g punnets strawberries, hulled, sliced widthways"
  echo "- 1/4 cup (60ml) Cointreau or other orange-flavoured liqueur"
  echo "- 1 1/2 cups (240g) Chelsea Icing Sugar, sifted"
  echo "- 300g mascarpone"
  echo "- 4 Tbsp Chelsea Caster Sugar"
  echo "- 2 eggs (59g each), at room temperature, separated"
  echo "- Sugar flowers or edible flowers, to decorate"
  echo
  echo -e "${CYAN}${BOLD}INSTRUCTIONS${CLEAR}"
  echo
  echo "Preheat oven to 170Â°C. Grease two 24cm cake tins and line bases with baking paper."
  echo ""
  echo "Cake"
  echo "Sift flour, baking powder and salt into a large bowl of an electric mixer. Add eggs, butter, caster sugar, and mandarin rind and juice, and beat on low speed until ingredients are moistened. Scrape down sides of bowl and beat for 1 minute or until mixture is smooth and well combined."
  echo ""
  echo "Divide mixture between tins. Smooth tops, then bake cakes for 30 minutes or until a skewer inserted into the centre comes out clean. Stand cakes in tins for 8 minutes, then turn on to a wire rack to cool completely."
  echo ""
  echo "Filling & Icing"
  echo "Combine strawberries, liqueur and 1/4 cup icing sugar in a bowl and set aside for 20 minutes."
  echo ""
  echo "Using an electric mixer, whisk mascarpone, 2 Tbsp caster sugar and 2 egg yolks until very thick. In a clean bowl, whisk 2 egg whites until soft peaks form. Gradually add remaining 2 Tbsp extra sugar and whisk until firm peaks form, then fold gently into mascarpone mixture."
  echo ""
  echo "Drain juices from strawberries and reserve. Place strawberries on one cake half and top with mascarpone mixture, spreading it 3cm from the edge. Top with second cake half."
  echo ""
  echo "Add enough strawberry juices to the remaining icing sugar to form a thick icing, then spread icing over the top of the cake, allowing it to drip down the sides slightly. Stand until icing is set, then decorate with sugar flowers or fresh edible flowers."

}