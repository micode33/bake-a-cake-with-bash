cheese_cake(){
  
  echo -e "${GREEN}${BOLD}CHEESE CAKE${CLEAR}"
  echo
  echo -e "${CYAN}${BOLD}INGREDIENTS${CLEAR}"
  echo
  echo -e "${YELLOW}${BOLD}Biscuit Base:${CLEAR}"
  echo "- 360g biscuits"
  echo "- 180g butter"
  echo
  echo -e "${YELLOW}${BOLD}Filling:${CLEAR}"
  echo "- 360g cream cheese"
  echo "- 1/2 cup Chelsea Caster Sugar"
  echo "- 1 tsp gelatine"
  echo "- 2 Tbsp water"
  echo "- 1 cup cream"
  echo "- 1/2 tsp vanilla essence"
  echo
  echo -e "${CYAN}${BOLD}INSTRUCTIONS${CLEAR}"
  echo
  echo "1. Prepare base in advance, by combining melted butter and biscuits."
  echo "2. Press into a greased cheesecake tin and refrigerate."
  echo
  echo "Filling"
  echo "3. Beat cream cheese and Chelsea Caster Sugar together."
  echo "4. Combine gelatine and water in the microwave, cool."
  echo "5. Add cooled gelatine to cream cheese mixture and beat together. Add essence."
  echo "6. Whip cream and fold into biscuit base and freeze."

}