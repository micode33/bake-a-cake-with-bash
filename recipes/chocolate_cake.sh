chocolate_cake(){
  
  echo -e "${GREEN}${BOLD}CHOCOLATE CAKE${CLEAR}"
  echo
  echo -e "${CYAN}${BOLD}INGREDIENTS${CLEAR}"
  echo
  echo -e "${YELLOW}${BOLD}Cake Base:${CLEAR}"
  echo "- 1 Tbsp instant coffee granules"
  echo "- ¾ cup water"
  echo "- 2 cups Chelsea White Sugar"
  echo "- 1¾ cups Edmonds Standard Grade Flour"
  echo "- ¾ cup cocoa powder"
  echo "- 2 tsp vanilla essence"
  echo "- 2 tsp Edmonds Baking Soda"
  echo "- 1 tsp Edmonds Baking Powder"
  echo "- ½ tsp salt"
  echo "- 2 eggs"
  echo "- 1¼ cups Meadow Fresh Milk (or Rice/Soya Milk)"
  echo "- ½ cup sunflower or soya oil"
  echo
  echo -e "${CYAN}${BOLD}INSTRUCTIONS${CLEAR}"
  echo
  echo "For NZ’s favourite chocolate cake recipe......."
  echo
  echo "1.  Preheat the oven to 180°C (reduce to 160 for fan forced). Grease and line a 23-25cm cake tin with baking paper."
  echo "2.  Dissolve the coffee granules in water, then place all ingredients into a food processor and process until well combined and smooth."
  echo "3.  Pour the mixture into the cake tin. Bake in the preheated oven for 1 hour 20 minutes to 1 hour 30 minutes, or until a cake tester inserted into the centre of the cake comes out clean."
  echo "4.  Cool on a wire rack and when completely cold, ice with either Chelsea Chocolate Icing Sugar or make your favourite with Chelsea Icing Sugar."
  echo
  echo "Note: This is a very runny mixture - don't be alarmed the first time you make it."

}