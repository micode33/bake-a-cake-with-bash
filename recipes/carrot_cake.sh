carrot_cake(){
  
  echo -e "${GREEN}${BOLD}CARROT CAKE${CLEAR}"
  echo
  echo -e "${CYAN}${BOLD}INGREDIENTS${CLEAR}"
  echo
  echo -e "${YELLOW}${BOLD}Cake Base${CLEAR}"
  echo "- 3 eggs"
  echo "- 2 cups Chelsea Soft Brown Sugar"
  echo "- 1 cup Olivani Olive Oil Pure"
  echo "- 3 cups grated carrots"
  echo "- 2 1/4 cups Edmonds Standard Grade Flour"
  echo "- 1/2 cup Meadow Fresh Original Milk (warmed)"
  echo "- 1 tsp salt"
  echo "- 2 tsp cinnamon"
  echo "- 2 tsp Edmonds Baking Soda"
  echo "- 1/2 cup walnuts"
  echo
  echo -e "${YELLOW}${BOLD}Cream Cheese Icing:${CLEAR}"
  echo "- 50g Tararua Butter, softened"
  echo "- 1/2 cup cream cheese"
  echo "- 2 cups Chelsea Icing Sugar"
  echo "- 1 tsp vanilla"
  echo "- 2-3 Tbsp Meadow Fresh Milk"
  echo
  echo -e "${YELLOW}${BOLD}For the milk chocolate frosting:${CLEAR}"
  echo "- 290 g unsalted butter softened to room temperature"
  echo "- 3-4 cups (360-480g) icing sugar"
  echo "- ¾ cup (65g) cocoa powder"
  echo "- 3-5 tbs (45-75ml) milk"
  echo "- 1 tsp vanilla extract"
  echo "- ¼ tsp salt optional"
  echo
  echo -e "${CYAN}${BOLD}INSTRUCTIONS${CLEAR}"
  echo
  echo "1. Preheat oven to 160°C bake."
  echo "2. Line a 23cm cake tin with baking paper and grease the sides and bottom."
  echo "3. Beat eggs and Chelsea Soft Brown Sugar together. Add oil then all other ingredients - mix well. Pour into cake tin."
  echo "4. Bake for 45 - 55 minutes, until a skewer inserted comes out clean. Stand in the tin for 20 mins then turn out and cool on a cake rack. Spread with cream cheese icing when cold."
  echo
  echo "Cream Cheese Icing"
  echo "5. Beat butter and cream cheese together. Add Chelsea Icing Sugar, vanilla and sufficient milk to make a light fluffy icing consistency. Spread over cake and decorate with chopped nuts if desired.echo"

}